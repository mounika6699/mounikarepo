#Converting different data types into String
print("The String form of integer 10 is:",str(10)) 
print(type(str(10)))   
print("The String form of Float 78.5 is:",str(78.5))
print(type(str(78.5)))
print("The String form of complex number 10+5j is:",str(10+5j)) 
print(type(str(10+5j)))  
print("The String form of boolean value True is:",str(True))
print(type(str(True)))